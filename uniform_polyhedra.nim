proc scale(p:Polyhedron):Polyhedron=
  var max=p.coords[0]
  result=p
  for c in result.coords: max=max(max,c.abs)
  for c in result.coords.mitems: c /= max 


when isMainModule:
  const CompiledScene = 1

  import opengl/glut, opengl, opengl/glu
  import random

  var
    n_poly {.global.} = Polyhedrons.high
    poly {.global.} = Polyhedrons[n_poly]

    zoom {.global.} = -4.0
    anglex {.global.}= 0.0
    angley {.global.}= 0.0
    lastx  {.global.}= 0
    lasty  {.global.}= 0

  converter v42gld(v:array[4,float32]):ptr GLfloat = cast[ptr GLfloat](v.unsafeAddr)
  converter v42gld(v:seq[float32]):ptr GLfloat = cast[ptr GLfloat](v[0].unsafeAddr)
  converter v42gld(v:array[1,float32]):ptr GLfloat = cast[ptr GLfloat](v.unsafeAddr)
  converter itogf(i:int):GLfloat=i.GLfloat

  proc draw_poly(p:Polyhedron)=
    var face : seq[int16] = @[]
    for i in p.faces:
      if i != -1: face.add i
      else:
        case face.len:
        of 3: glColor3f(1,0,0)
        of 4: glColor3f(0,1,0)
        of 5: glColor3f(0,0,1)
        else: glColor3f(0.7,0.3,0.6)
        
        glBegin(GL_POLYGON) # faces
        for f in face: glVertex3fv(p.coords[f*3..f*3+2])
        glEnd()

        glColor3f(0,0,0) # lines
        glBegin(GL_LINE_LOOP)
        for f in face: glVertex3fv(p.coords[f*3..f*3+2])
        glEnd()

        face.setLen 0

  proc create_list() {.cdecl.} =
    glNewList(CompiledScene, GL_COMPILE) # list 1 is scene
    draw_poly(poly.scale)
    glEndList()
    glutSetWindowTitle "uniform polyhedra - " & poly.name


  proc draw_scene() {.cdecl.} =
    glTranslatef(0, 0, zoom)
    glRotatef(anglex, 1, 0, 0)
    glRotatef(-angley, 0, 1, 0)

    glCallList(CompiledScene)
  
  proc reshape(width: GLsizei, height: GLsizei) {.cdecl.} =
    if height != 0:
      glViewport(0, 0, width, height)
      glMatrixMode(GL_PROJECTION) # To operate on the Projection matrix
      glLoadIdentity() # Reset
      gluPerspective(45.0, width / height, 0.1, 100.0)

  proc keyhook(key: GLbyte, x, y: GLint) {.cdecl.} =
    case key.char:
    of 'q', '\e': quit(1)
    of '+':
      if n_poly < Polyhedrons.high: n_poly.inc
      else: n_poly=0
      poly = Polyhedrons[n_poly]  
    of '-':
      if n_poly > 0: n_poly.dec
      else: n_poly=Polyhedrons.high
      poly = Polyhedrons[n_poly]
    of ' ' : 
      anglex = 0
      angley = 0

    else: discard

    create_list()

  proc mouseDrag(x,y:GLint) {.cdecl.} =
    let d = 2.0
    angley += (x-lastx).float / d
    anglex += (y-lasty).float / d  

    lastx=x
    lasty=y

  proc mouseWheel(button, dir, x,y:GLint) {.cdecl.} =
    case button:
        of 3: zoom+=0.1
        of 4: zoom-=0.1
        else: discard

  proc display() {.cdecl.} =
    glClear(GL_COLOR_BUFFER_BIT or GL_DEPTH_BUFFER_BIT) # Clear color and depth buffers
    glMatrixMode(GL_MODELVIEW) # To operate on model-view matrix
    glLoadIdentity() # Reset the model-view matrix

    draw_scene()

    glutSwapBuffers()
    glutPostRedisplay() # animate

  
  proc main_gl =

    glutInit()
    
    glutInitDisplayMode(GLUT_DOUBLE or GLUT_RGB or GLUT_DEPTH)

    glutInitWindowSize(1200, 1200)
    
    discard glutCreateWindow "uniform polyhedra"

    glutDisplayFunc(display)
    glutReshapeFunc(reshape)
    glutKeyboardFunc(keyhook)
    glutMouseFunc(mouseWheel)
    glutMotionFunc(mouseDrag)

    loadExtensions()

    glClearColor(0,0,0, 1) # Set background color to black and opaque
    glClearDepth(1) # Set background depth to farthest

    glEnable(GL_LINE_SMOOTH)
    glHint(GL_LINE_SMOOTH_HINT, GL_NICEST)
    glHint(GL_POLYGON_SMOOTH_HINT, GL_NICEST)
    glEnable(GL_DEPTH_TEST)

    glLineWidth(2)

    create_list()
    glutMainLoop()

  main_gl()